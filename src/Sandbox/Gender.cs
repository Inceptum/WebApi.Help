namespace Sandbox
{
    /// <summary>
    /// A type to represent a gender of the user.
    /// </summary>
    /// <summary lang="ru">
    /// Тип для представления пола пользователя.
    /// </summary>
    public enum Gender
    {
        /// <summary>
        /// The gender is unknown.
        /// </summary>
        /// <summary lang="ru">
        /// Пол неизвесен.
        /// </summary>
        Unknown = 0,
        /// <summary>
        /// Male gender
        /// </summary>
        /// <summary lang="ru">
        /// Мужчина.
        /// </summary>
        Male = 1,
        /// <summary>
        /// Female gender
        /// </summary>
        /// <summary lang="ru">
        /// Женщина
        /// </summary>
        Female = 2,
        /// <summary>
        /// Androgynous gender (both male and female)
        /// </summary>
        /// <summary lang="ru">
        /// Двуполое существо.
        /// </summary>
        Bisexual = 3
    }
}